// if-else statement
let grade = 80;
if (grade >= 90) {
  console.log("A");
} else if (grade >= 80) {
  console.log("B");
} else {
  console.log("C");
}

// switch statement
let day = "Monday";
switch (day) {
  case "Monday":
    console.log("It's the start of the week");
    break;
  case "Friday":
    console.log("Weekend is near");
    break;
  default:
    console.log("It's a regular day");
}
